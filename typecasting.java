public class typecasting {
    public static void main(String[]args)
    {
        
    
        int i1 = 9;
        double d1 = i1;
        System.out.println(i1);
        System.out.println(d1);



        //manual
        double d2 =9.78d;
        int i2 = (int) d2;
        System.out.println(d2);
        System.out.println(i2);


    }
}
